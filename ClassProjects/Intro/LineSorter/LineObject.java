
public class LineObject {

	public int x;
	public int y;
	
	public LineObject(int xvalue, int yvalue)
	{
		x = xvalue;
		y = yvalue;
	}
	
	public int getX()
	{
		return x;
	}
	
	public int getY()
	{
		return y;
	}
	
	public void setX(int xvalue)
	{
		x = xvalue;
	}
	
	public void setY(int yvalue)
	{
		y = yvalue;
	}
	
}
